public class add {
    public static void main(String[] args) {
        String num1 = "11";
        String num2 = "123";

        // Convert strings to integers
        int n1 = Integer.parseInt(num1);
        int n2 = Integer.parseInt(num2);

        // Add them
        int sum = n1 + n2;

        // Print the result
        System.out.println(sum); // Output: 134
    }
}
